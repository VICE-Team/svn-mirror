{
    char *p = xmalloc(1024);

    /* Yeah, this sucks, but you have readline anyway, don't you?  ;-) */
    fputs(prompt, mon_output);
    fflush(mon_output);
    fgets(p, 1024, mon_input);

    /* Remove trailing newlines.  */
    {
        int len;

        for (len = strlen(p);
             len > 0 && (p[len - 1] == '\r'
                         || p[len - 1] == '\n');
             len--)
            p[len - 1] = '\0';
    }

    return p;
}

