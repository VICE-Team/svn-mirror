void mon_check_icount(ADDRESS a)
{
    if (instruction_count) {
        if (!wait_for_return_level)
           instruction_count--;

        if (GET_OPCODE(caller_space) == OP_JSR)
           wait_for_return_level++;

        if (GET_OPCODE(caller_space) == OP_RTS)
           wait_for_return_level--;

        /* FIXME: Should this set the return level to 0? */
        if (GET_OPCODE(caller_space) == OP_RTI)
           wait_for_return_level--;

        if (!instruction_count) {
           if (mon_mask[caller_space] & MI_STEP) {
              mon_mask[caller_space] &= ~MI_STEP;
              disassemble_instr(new_addr(caller_space, a));
           }
           if (!mon_mask[caller_space])
              monitor_trap_off(mon_interfaces[caller_space]->int_status);

           mon(a);
        }
    }
}

void mon_check_watchpoints(ADDRESS a)
{
    if (watch_load_occurred) {
        if (watchpoints_check_loads(e_comp_space)) {
            caller_space = e_comp_space;
            mon(a);
        }
        if (watchpoints_check_loads(e_disk8_space)) {
            caller_space = e_disk8_space;
            mon(a);
        }
        if (watchpoints_check_loads(e_disk9_space)) {
            caller_space = e_disk9_space;
            mon(a);
        }
        watch_load_occurred = FALSE;
    }

    if (watch_store_occurred) {
        if (watchpoints_check_stores(e_comp_space)) {
            caller_space = e_comp_space;
            mon(a);
        }
        if (watchpoints_check_stores(e_disk8_space)) {
            caller_space = e_disk8_space;
            mon(a);
        }
        if (watchpoints_check_stores(e_disk9_space)) {
            caller_space = e_disk9_space;
            mon(a);
        }
        watch_store_occurred = FALSE;
    }
}

static void make_prompt(char *str)
{
    sprintf(str, "(%s:$%04x) ",
            memspace_string[default_memspace],
            addr_location(dot_addr[default_memspace]));
}

static signal_handler_t old_handler;

static void handle_abort(int signo)
{
  stop_output = 1;
  signal(SIGINT, (signal_handler_t) handle_abort);
}

void mon(ADDRESS a)
{
   char prompt[40];

#ifdef __MSDOS__
    int old_input_mode, old_output_mode;

    enable_text();
    clrscr();
    _set_screen_lines(43);
    _setcursortype(_SOLIDCURSOR);

    old_input_mode = setmode(STDIN_FILENO, O_TEXT);
    old_output_mode = setmode(STDOUT_FILENO, O_TEXT);

    mon_output = fopen("CON", "wt");
    mon_input = fopen("CON", "rt");
    setbuf(mon_output, NULL);    /* No buffering */
#elif defined WIN32
    AllocConsole();
    mon_output = fopen("CON", "wt");
    mon_input = fopen("CON", "rt");
#else
    mon_output = stdout;
    mon_input = stdin;
#endif

    old_handler = signal(SIGINT, handle_abort);
#ifdef __riscos
    Wimp_CommandWindow((int)"Vice Monitor");
#endif

    inside_monitor = TRUE;
    suspend_speed_eval();

    dot_addr[caller_space] = new_addr(caller_space, a);

    fprintf(mon_output, "\n** Monitor\n");
    fflush(mon_output);

    while (!exit_mon) {
        make_prompt(prompt);

        if (asm_mode) {
            sprintf(prompt,".%04x  ", addr_location(asm_mode_addr));
        }

        myinput = readline(prompt);
        stop_output = 0;
        if (myinput == NULL) {
            fprintf(mon_output, "\n");
        } else {
            if (!myinput[0]) {
                if (!asm_mode) {
                    /* Repeat previous command */
                    free(myinput);

                    if (last_cmd)
                        myinput = stralloc(last_cmd);
                    else
                        myinput = NULL;
                } else {
                    /* Leave asm mode */
                    make_prompt(prompt);
                }
            } else {
                /* Nonempty line */
                add_history(myinput);
            }

            if (myinput) {
                if (recording) {
                    if (fprintf(recording_fp, "%s\n", myinput) != 1) {
                       fprintf(mon_output,"Error while recording commands. Output file closed.\n");
                       fclose(recording_fp);
                       recording_fp = NULL;
                       recording = FALSE;
                    }
                }

                parse_and_execute_line(myinput);

                if (playback)
                    playback_commands(playback_name);
            }
        }
        if (last_cmd)
            free(last_cmd);

        last_cmd = myinput;
    }
    inside_monitor = FALSE;
    suspend_speed_eval();

    exit_mon--;

    if (exit_mon)
        exit(0);

#ifdef __riscos
    Wimp_CommandWindow(-1);
#endif
    signal(SIGINT, old_handler);

#ifdef __MSDOS__
    setmode(STDIN_FILENO, old_input_mode);
    setmode(STDIN_FILENO, old_output_mode);

    disable_text();

    fclose(mon_input);
    fclose(mon_output);
#endif
#ifdef WIN32
    FreeConsole();
#endif
}
